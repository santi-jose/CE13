#include "SimpleMath.h"

int SquarePlusOne(int x){
    //declare local temporary variable
    int temp;
    
    //set equal to x for math operations
    temp = x;
    
    //square temp;
    temp = temp * temp;
    
    //add one
    temp++;
}

